public class Paquete {
    
    int n_acciones;
    int valor_accion;

    public Paquete (int n_acciones, int valor_accion){
        this.n_acciones = n_acciones;
        this.valor_accion = valor_accion;
    }

    public int getN_acciones() {
        return n_acciones;
    }
    public int getValor_accion() {
        return valor_accion;
    }
}
